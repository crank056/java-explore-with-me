DROP TABLE IF EXISTS EVENTS, CATEGORIES, LOCATIONS, USERS,
    PARTICIPATIONS_REQUESTS, COMPILATIONS, EVENTS_COMPILATIONS, COMMENTS, COMMENT_IGNORE_LIST,
    RATING_LIST;

CREATE TABLE IF NOT EXISTS CATEGORIES
(
    id   BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    CONSTRAINT UQ_NAME UNIQUE (name)
);

CREATE TABLE IF NOT EXISTS LOCATIONS
(
    id  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    lat FLOAT8 NOT NULL,
    lon FLOAT8 NOT NULL
);

CREATE TABLE IF NOT EXISTS USERS
(
    id    BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name  VARCHAR(100) NOT NULL,
    email VARCHAR(250) NOT NULL,
    CONSTRAINT UQ_USER_EMAIL UNIQUE (email)
);

CREATE TABLE IF NOT EXISTS COMMENT_IGNORE_LIST
(
    user_id       BIGINT REFERENCES USERS (id) ON DELETE CASCADE,
    speechless_id BIGINT REFERENCES USERS (id) ON DELETE CASCADE,
    primary key (user_id, speechless_id)
);

CREATE TABLE IF NOT EXISTS COMPILATIONS
(
    id     BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    pinned BOOLEAN      NOT NULL,
    title  VARCHAR(250) NOT NULL
);

CREATE TABLE IF NOT EXISTS EVENTS
(
    id                 BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY NOT NULL,
    title              VARCHAR(1000),
    annotation         VARCHAR(1000),
    category_id        BIGINT REFERENCES CATEGORIES (id) ON DELETE CASCADE,
    description        VARCHAR(1000),
    created_on         TIMESTAMP WITHOUT TIME ZONE,
    event_date         TIMESTAMP WITHOUT TIME ZONE,
    published_on       TIMESTAMP WITHOUT TIME ZONE,
    location_id        BIGINT REFERENCES LOCATIONS (id) ON DELETE CASCADE,
    paid               BOOLEAN,
    participant_limit  BIGINT,
    request_moderation BOOLEAN,
    confirmed_requests INTEGER,
    initiator_id       BIGINT REFERENCES USERS (id) ON DELETE CASCADE,
    state              VARCHAR,
    views              BIGINT
);

CREATE TABLE IF NOT EXISTS EVENTS_COMPILATIONS
(
    compilation_id BIGINT REFERENCES COMPILATIONS (id),
    event_id       BIGINT REFERENCES EVENTS (id),
    primary key (compilation_id, event_id)
);

CREATE TABLE IF NOT EXISTS PARTICIPATIONS_REQUESTS
(
    id           BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created      TIMESTAMP NOT NULL,
    event_id     BIGINT REFERENCES EVENTS (id) ON DELETE CASCADE,
    requester_id BIGINT REFERENCES USERS (id) ON DELETE CASCADE,
    status       VARCHAR   NOT NULL
);

CREATE TABLE IF NOT EXISTS COMMENTS
(
    id             BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY NOT NULL,
    description    VARCHAR(2000),
    created        TIMESTAMP WITHOUT TIME ZONE,
    commentator_id BIGINT REFERENCES USERS (id) ON DELETE CASCADE,
    event_id       BIGINT REFERENCES EVENTS (id) ON DELETE CASCADE,
    rate           BIGINT                                              NOT NULL
);

CREATE TABLE IF NOT EXISTS RATING_LIST
(
    user_id       BIGINT REFERENCES USERS (id) ON DELETE CASCADE,
    comment_id BIGINT REFERENCES COMMENTS (id) ON DELETE CASCADE,
    primary key (user_id, comment_id)
);